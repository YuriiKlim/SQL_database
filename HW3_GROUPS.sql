-- СТВОРЕННЯ ТАБЛИЦІ ГРУПИ (GROUPS)
CREATE TABLE GROUPS (
    ID SERIAL PRIMARY KEY,
    NAME VARCHAR(10) NOT NULL UNIQUE CHECK (NAME <> ''),
    RATING INT NOT NULL CHECK (RATING >= 0 AND RATING <= 5),
    YEAR INT NOT NULL CHECK (YEAR >= 1 AND YEAR <= 5)
);

