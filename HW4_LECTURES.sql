-- СТВОРЕННЯ ТАБЛИЦІ ЛЕКЦІЇ (LECTURES)
CREATE TABLE LECTURES (
    ID SERIAL PRIMARY KEY,
    LECTURE_ROOM VARCHAR NOT NULL CHECK (LECTURE_ROOM <> ''),
    SUBJECT_ID INT NOT NULL,
    TEACHER_ID INT NOT NULL
);

