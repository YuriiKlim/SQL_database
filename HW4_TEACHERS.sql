    ID SERIAL PRIMARY KEY,
    NAME VARCHAR NOT NULL CHECK (NAME <> ''),
    SURNAME VARCHAR NOT NULL CHECK (SURNAME <> ''),
    SALARY DECIMAL(10, 2) NOT NULL CHECK (SALARY > 0)
);

