    ID SERIAL PRIMARY KEY,
    BUILDING INT NOT NULL CHECK (BUILDING >= 1 AND BUILDING <= 5), 
	FLOOR INT NOT NULL CHECK (FLOOR >= 1),
    NAME VARCHAR(20) NOT NULL UNIQUE CHECK (NAME <> '')
);

