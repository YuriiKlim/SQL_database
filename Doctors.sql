	ID SERIAL PRIMARY KEY,
	NAME VARCHAR(255) NOT NULL CHECK (NAME <> ''),
    SURNAME VARCHAR(255) NOT NULL CHECK (SURNAME <> ''),
	PHONE CHAR(10),
    SALARY NUMERIC NOT NULL CHECK (SALARY > 0)
);

